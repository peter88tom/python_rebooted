"""
Python list is just like array which is an ordered collection of data
Very flexible as the items in a list do not need to be of the same type
"""
List = [1, 2, 3, "one two three", 4.6]
print(List)

"""
List elements can be accessed by index
In python index start at 0
"""
# Here we want to print 2 from the list, we can access this by list[1]
print(List[1])


